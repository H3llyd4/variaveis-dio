

// estrutura de decisão
// switch/case/break/default

let  fruta = "abacaxi"

switch (fruta) {
    case "abacaxi":
        console.log("Suco de Abacaxi.")
    break
    case "laranja":
        console.log("Suco de Laranja.")
    break
    case "banana":
        console.log("Vitamina de Banana.")
    break
    case "morango":
        console.log("Suco de Morango.")
    case "maçã":
        console.log("Suco de Maçã.")
    break
    default:
        console.log("Não terá vitamina ou suco")
    break
}